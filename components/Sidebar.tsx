/* ── Navigation Config ──────────────────────────────────────────────── */

const nav = [
  { label: "Dashboard", href: "/dashboard", icon: DashboardIcon },
  { label: "Record Visit", href: "/record", icon: MicIcon },
  { label: "Call Logs", href: "/calllogs", icon: ClipboardIcon },
  { label: "Follow-Ups", href: "/followups", icon: BellIcon },
  { label: "Reports", href: "/reports", icon: TrendingUpIcon },
];

/* ── Sidebar Component ──────────────────────────────────────────────── */

export default function Sidebar() {
  const pathname = usePathname();
  const router = useRouter();

  async function handleLogout() {
    const supabase = createClient();
    await supabase.auth.signOut();
    router.push("/login");
  }

  return (
    <aside className="flex h-screen w-64 flex-col bg-gradient-sidebar">
      {/* Brand */}
      <div className="flex h-16 items-center gap-2 border-b border-white/10 px-6">
        <span className="text-xl font-bold text-white">HRI</span>
        <span className="text-sm text-teal-300">Referral Intel</span>
      </div>

      {/* Navigation */}
      <nav className="flex-1 space-y-1 px-3 py-4">
        {nav.map((item) => {
          const active = pathname === item.href;
          const Icon = item.icon;
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex items-center gap-3 rounded-lg px-3 py-2.5 text-sm font-medium transition-colors ${
                active
                  ? "bg-white/10 text-white border-l-2 border-teal-400"
                  : "text-gray-300 hover:bg-white/5 hover:text-white"
              }`}
            >
              <Icon className="h-5 w-5" />
              {item.label}
            </Link>
          );
        })}
      </nav>

      {/* Logout */}
      <div className="border-t border-white/10 p-4">
        <button
          onClick={handleLogout}
          className="flex w-full items-center gap-3 rounded-lg px-3 py-2 text-sm font-medium text-gray-300 hover:bg-white/10 hover:text-white transition-colors"
        >
          <LogOutIcon className="h-5 w-5" />
          Sign Out
        </button>
      </div>
    </aside>
  );
}
